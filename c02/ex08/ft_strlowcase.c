char	*ft_strlowcase(char *str)
{
	int		j;
	char	x;

	j = 0;
	while (str[j] != '\0')
	{
		x = str[j];
		if ((str[j] > 64 && (str[j] < 91) && str[j] != 0))
		{
			str[j] = x + 32;
		}
		j++;
	}
	return (str);
}
