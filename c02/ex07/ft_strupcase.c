char	*ft_strupcase(char *str)
{
	int		j;
	char	x;

	j = 0;
	while (str[j] != '\0')
	{
		x = str[j];
		if (str[j] > 96 && str[j] < 123)
		{
			str[j] = x - 32;
		}
		j++;
	}
	return (str);
}
